numeros = [45, 12, 89, 3, 27, 56, 91, 14, 7, 66, 38, 22, 5, 73, 10]

print("Array original:")
print(numeros)

numeros.sort()

print("\nOrdem crescente:")
print(numeros)

numeros.sort(reverse=True)

print("\nOrdem decrescente:")
print(numeros)

